#function for getting norep list of names and times
def getNoRepLists(timelist, namelist):
    norepnames = []
    noreptimes = []
    for i in range(len(namelist)):
        if namelist[i] in norepnames:
            continue
        else:
            norepnames.append(namelist[i])
            noreptimes.append(timelist[i])
    return noreptimes, norepnames

